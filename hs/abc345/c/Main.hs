-- 参考にさせていただく
-- https://atcoder.jp/contests/abc345/editorial/9561
solve :: String -> Int
solve s =
  let n = length s
      m = countElements s
      minus = M.foldl (\a b -> a + (b ^ 2)) 0 m
      r = ((n ^ 2) - minus) `div` 2
   in if 1 <= (M.size . M.filter (2 <=) $ m)
        then succ r -- 被る文字がある場合は元の文字列自体になり得るので+1
        else r

-- リストの各要素を数える
countElements :: (Ord a) => [a] -> M.Map a Int
countElements = M.fromList . map count . group . sort
  where
    count xs = (head xs, length xs)

-- nCr は 組み合わせ (combination)　の計算
-- n個からr個選ぶ場合の組み合わせの数を求めるときに利用する
nCr :: Int -> Int -> Int
nCr n r =
  let numerator = product $ take r [n, n - 1 ..]
      denominator = product $ take r [1 ..]
   in numerator `div` denominator

-- nCrのうち、2個選ぶケースは多いので別で用意しておく
nC2 :: Int -> Int
nC2 n = n * (n + 1) `div` 2

