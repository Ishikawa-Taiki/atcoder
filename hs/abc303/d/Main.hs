main :: IO ()
main = do
  op <- listToTuple3 . bsToIntegerList <$> BS.getLine
  xs <- getLineToString
  let l = length xs
      dp = solve xs l op
  print $ min (dp ! (l, 0)) (dp ! (l, 1))

{-
問題概要
A,aで構成される文字列と、aキーを押す時間、Shiftキーを押す時間、CapsLockキーを押す時間X,Y,Zが与えられる
文字列を一致させるのにかかる最短の時間を求めよ
最初はCapsLockはオフの状態となる

戦略
文字列が一致するまでにかかる時間について、CapsLockオンオフそれぞれの状態分を動的計画法でも求める

-}

type OP = (Integer, Integer, Integer)

solve :: [Char] -> Int -> OP -> Array (Int, Int) Integer
solve xs l op = dp
  where
    s = listArray @Array (1, l) $ fmap (bool 0 1 . (== 'A')) xs
    dp =
      listArray @Array
        ((0, 0), (l, 1))
        [ calc s op dp i
          | i <-
              [ (pos, caps)
                | pos <- [1 .. l],
                  caps <- [0, 1]
              ]
        ]

calc :: Array Int Int -> OP -> Array (Int, Int) Integer -> (Int, Int) -> Integer
-- calc s op@(x, y, z) dp i@(pos, caps) = undefined
calc s op dp (0, 0) = 0
calc s op dp (0, 1) = thd3 op
calc s op@(x, y, z) dp (pos, caps) = min capsChangeCost noCapsChangeCost
  where
    needsShift = s ! pos /= caps
    noCapsChangeCost = bool aCost shiftAndACost needsShift
    aCost = dp ! (pred pos, caps) + x
    shiftAndACost = dp ! (pred pos, notCaps) + y
    capsChangeCost = dp ! (pos, notCaps) + z
    notCaps = bool 1 0 $ caps == 1

